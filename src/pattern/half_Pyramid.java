// Half Pyramid


package src.pattern;

public class half_Pyramid {

    public static void main(String[] args) {
        
        

        for (int i = 1; i <= 4; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("\uD83D\uDC26");
            }
            System.out.println();
        }

    }
    
}
